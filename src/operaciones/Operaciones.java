package operaciones;

//Sobrecarga de Metodos
public class Operaciones {

    //Sumar int
    public static int sumar(int a, int b){
        return a+b;
    }

    //Sumar double. Se puede llamar igual pero debe cambiar el tipo de dato que recibe.
    public static double sumar(double a, double b) {
        return a+b;
    }

}
